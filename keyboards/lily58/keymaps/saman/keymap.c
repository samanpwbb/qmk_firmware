void oled_task_user(void) {
  if (is_keyboard_master()) {
    if (layer_state_is(_QWERTY)) {
      oled_write_raw_P(art1, sizeof(art1));
    } else {
      oled_write_raw_P(art3, sizeof(art3));
    }
  } else {
    oled_write_raw_P(art2, sizeof(art2));
  }
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {

  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case DESIGN:
      if (record->event.pressed) {
        layer_on(_DESIGN);
      } else {
        layer_off(_DESIGN);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
      } else {
        layer_off(_RAISE);
      }
      return false;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) { /* First encoder */
        if (clockwise) {
            if (layer_state_is(_RAISE)) {
                tap_code(KC_PGDN);
            } else if (layer_state_is(_DESIGN)) {
                tap_code16(LGUI(KC_KP_PLUS));
            } else {
                tap_code(KC_VOLU);
            }
        } else {
            if (layer_state_is(_RAISE)) {
                tap_code(KC_PGUP);
            } else if (layer_state_is(_DESIGN)) {
                tap_code16(LGUI(KC_MINS));
            } else {
                tap_code(KC_VOLD);
            }
        }
    }
}
